import sys
import os

try:
    # 1. Проверяем, что нам передали ровно один аргумент (имя файла)
    if len(sys.argv) != 2:
        # Выводим сообщение об ошибке в stderr (правильное место для ошибок!)
        sys.stderr.write("Ошибка: Укажите ровно один аргумент - имя файла.\n")
        sys.exit(1) # Код 1 - ошибка использования

    filename = sys.argv[1]

    # 2. Проверяем, существует ли файл
    if not os.path.exists(filename):
        sys.stderr.write(f"Ошибка: Файл '{filename}' не найден.\n")
        sys.exit(2) # Код 2 - файл не найден

    # 3. Если все проверки пройдены
    print(f"Успех: Файл '{filename}' существует.")
    sys.exit(0) # Код 0 - все в порядке!
finally:
    print("-" * 80)