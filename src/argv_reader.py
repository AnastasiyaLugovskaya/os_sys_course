import sys


if __name__ == '__main__':
    print("--- Скрипт запущен. Вскрываю конверт sys.argv... ---")

    # Конверт sys.argv - это обычный список
    print(f"Вот что я нашел в конверте: {sys.argv}")

    # Давайте посмотрим на каждую "бумажку" отдельно
    print("\n--- Содержимое конверта по порядку: ---")

    # Первая бумажка (индекс 0) - мое имя
    script_name = sys.argv[0]
    print(f"Бумажка №0: Имя скрипта - '{script_name}'")

    # А теперь посмотрим на остальные инструкции, если они есть
    # len(sys.argv) > 1 означает "если в конверте больше одной бумажки"
    if len(sys.argv) > 1:
        first_instruction = sys.argv[1]
        print(f"Бумажка №1: Первая инструкция - '{first_instruction}'")
    else:
        print("Больше никаких инструкций не было.")
    print("-" * 80)